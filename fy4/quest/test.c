//Find the First Index to have exp level >= x
int Find(int x)
{
        int total;
        int low,high,now;
        total=sizeof(quest); 
        if(quest[0]["exp_bonus"]>=x)
                return 0;
        if(quest[total-1]["exp_bonus"]<x)
                return total-1; 
        low=0;
        high=total-1;
        now=(low+high)/2; 
        while(high-low>1) {
                if(quest[now]["exp_bonus"]>=x)
                        high=now;
                else 
                        low=now;
                now=(low+high)/2;
        }
        return high;
} 
mapping query_quest(int exp)
{
        int a,b,c; 
        int low,high; 
        if(exp>1000) {
                low=exp-200; 
                high=exp+600;
        }
        else {
                low=exp*8/10;
                high=exp*16/10;
        } 
        if(low>5000) low=5000;   
        if(high<3) high=3; 

        a=Find(low);
        b=Find(high)-1;
        c=a+random(b+1-a);
        return quest[c];
} 
